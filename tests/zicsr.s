
############################################################
# Test 1 - CSRRW
############################################################

li      t0, 0x12345678
csrrw   t1, mscratch, t0

# t1 = old mscratch (unknown)
# mscratch = 0x12345678

csrr    t2, mscratch

# EXPECT:
# t2 = 0x12345678


############################################################
# Test 2 - CSRRS
############################################################

li      t0, 0x0000000F
csrrs   t3, mscratch, t0

# old = 0x12345678
# new = old | 0xF

csrr    t4, mscratch

# EXPECT
# t3 = 0x12345678
# t4 = 0x1234567F


############################################################
# Test 3 - CSRRC
############################################################

li      t0, 0x0000000C
csrrc   t5, mscratch, t0

csrr    t6, mscratch

# EXPECT
# t5 = 0x1234567F
# t6 = 0x12345673


############################################################
# Test 4 - CSRRWI
############################################################

csrrwi  s0, mscratch, 21

csrr    s1, mscratch

# EXPECT
# s1 = 21


############################################################
# Test 5 - CSRRSI
############################################################

csrsi   mscratch, 10

csrr    s2, mscratch

# EXPECT
# s2 = 31
#
# 21 | 10 = 31


############################################################
# Test 6 - CSRRCI
############################################################

csrrci  s3, mscratch, 3

csrr    s4, mscratch

# EXPECT
# s3 = 31
# s4 = 28


############################################################
# Test 7 - rs1 = x0 (read only)
############################################################

csrrs   s5, mscratch, x0

# EXPECT
# s5 = 28
# mscratch unchanged


